    val maxByteValue: Byte = Byte.MAX_VALUE
    val minByteValue: Byte = Byte.MIN_VALUE
    println("Byte maximum value is: $maxByteValue")
    println("Byte minimum value is: $minByteValue")

    val maxShortValue: Short = Short.MAX_VALUE
    val minShortValue: Short = Short.MIN_VALUE
    println("Short maximum value is: $maxShortValue")
    println("Short minimum value is: $minShortValue")

    val maxIntegerValue: Int = Int.MAX_VALUE
    val minIntegerValue: Int = Int.MIN_VALUE
    println("Int maximum value is: $maxIntegerValue")
    println("Int minimum value is: $minIntegerValue")

    val maxFloatValue: Float = Float.MAX_VALUE
    val minFloatValue: Float = Float.MIN_VALUE
    println("Float maximum value is: $maxFloatValue")
    println("Float minimum value is: $minFloatValue")

    val maxDoubleValue: Double = Double.MAX_VALUE
    val minDoubleValue: Double = Double.MIN_VALUE
    println("Double maximum value is: $maxDoubleValue")
    println("Double minimum value is: $minDoubleValue")

}